int	ft_iterative_factorial(int nb)
{
	int a;
	int b;

	a = 1;
	b = 1;
	if ((nb < 0) || (nb > 12))
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	while (b <= nb)
	{
		a = b * a;
		b++;
	}
	return (a);
}
