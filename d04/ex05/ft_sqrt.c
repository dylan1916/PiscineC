int	ft_sqrt(int nb)
{
	int a;
	int b;

	a = 1;
	b = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (a <= nb)
	{
		a++;
		b = a * a;
		if (b == nb)
			return (a);
	}
	return (0);
}
